DROP table Products;
CREATE TABLE Products(
                       id int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       name varchar(100) NOT NULL,
                       quality int,
                       price float,
                       year int,
                       producer varchar(100)
);

